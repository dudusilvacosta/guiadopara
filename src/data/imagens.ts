export function getTiposImagens() {
  const tiposUnicos = Array.from(
    new Map(
      imagens.map((img) => [
        img.slug,
        {
          slug: img.slug,
          tipo: img.tipo,
        },
      ])
    ).values()
  );

  return tiposUnicos;
}

export function getImagensPorTipo(slug: string) {
  return imagens.filter(
    (img) => img.slug.toLowerCase() === slug.toLowerCase()
  );
};

export function getImagemPorId(id: number) {
  return imagens.find((img) => img.id === id);
};

export function getPesquisa(termo: string) {
  if (!termo.trim()) {
    return [];
  }

  return imagens.filter((img) =>
    img.nome.toLowerCase().includes(termo.toLowerCase())
  );
};